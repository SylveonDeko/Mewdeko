/// <summary>
///     Represents different booru platforms used for downloading images.
/// </summary>
public enum Booru
{
    /// <summary>
    ///     Represents Safebooru, a safe-for-work booru platform.
    /// </summary>
    Safebooru,

    /// <summary>
    ///     Represents E621, an explicit booru platform.
    /// </summary>
    E621,

    /// <summary>
    ///     Represents Derpibooru, a booru platform dedicated to My Little Pony content.
    /// </summary>
    Derpibooru,

    /// <summary>
    ///     Represents Rule34, a booru platform known for explicit content based on various media.
    /// </summary>
    Rule34,

    /// <summary>
    ///     Represents Gelbooru, a booru platform known for its large collection of images.
    /// </summary>
    Gelbooru,

    /// <summary>
    ///     Represents Konachan, a booru platform focused on anime images.
    /// </summary>
    Konachan,

    /// <summary>
    ///     Represents Yandere, a booru platform specializing in anime-style artwork.
    /// </summary>
    Yandere,

    /// <summary>
    ///     Represents Danbooru, a popular booru platform primarily focused on anime and manga.
    /// </summary>
    Danbooru,

    /// <summary>
    ///     Represents Sankaku Complex, a booru platform known for its large collection of anime images.
    /// </summary>
    Sankaku,

    /// <summary>
    ///     Represents Realbooru, a booru platform featuring a variety of real-life and fictional images.
    /// </summary>
    Realbooru
}